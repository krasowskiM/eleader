public class IphoneRequest {
    private int day;
    private int hour;
    private int minute;
    private int month;
    private int numberOfGuests;
    private int year;

    public IphoneRequest() {
    }

    public int getDay() {
        return day;
    }

    public int getHour() {
        return hour;
    }

    public int getMinute() {
        return minute;
    }

    public int getMonth() {
        return month;
    }

    public int getNumberOfGuests() {
        return numberOfGuests;
    }

    public int getYear() {
        return year;
    }
}
